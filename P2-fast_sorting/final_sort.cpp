void sortDataList(list<Data *> &l) {
  // Fill this in
  front = l.front()->lastName;
  back = l.back()->lastName;

  if (front[0]=='A' && back[0]=='Z') {
    // Assume T3
    sortT3(l);
  }

  else if (front==back) {
    // Assume T4
    radixSort(l);
  }

  else {
    // Assume T1 or T2
    sortByName(l);
  }
}
