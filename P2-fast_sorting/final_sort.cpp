int hashLast(string name) { // Unique for last names, not first

  size = static_cast<int>(name.size());
  x;
  value = 0;

  for (int c=0; c<size; c++) {
    x = int(name[c]) - 78;
    value = (value + (((value/10)*(value/10))+1)+(((x*size)+1)*((x*c)+1)+1)) % 100000; // works for lastName
  }
  return value;
}

